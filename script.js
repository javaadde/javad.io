function OpenNev(){
    document.getElementById("nav-mob").style.display = "flex"
    document.getElementById("m-i").style.display = "none"
    document.getElementById("m-ci").style.display = "flex"
}

function CloseNev(){
    document.getElementById("nav-mob").style.display = "none"
    document.getElementById("m-ci").style.display = "none"
    document.getElementById("m-i").style.display = "flex"
}